              <li>Thông tin quan trọng nhất là       đường link trực tiếp đến bài báo. Do số lượng bài báo mỗi người cung cấp cần       phải xử lý mỗi ngày là không ít, cung cấp đường link trực tiếp đến bài báo       là cách tốt nhất để tiết kiệm thời gian và công sức cho họ. Cũng bằng cách       đó người yêu cầu có thể yêu cầu số lượng không giới hạn các bài báo mình cần.       Đường link trực tiếp được hiểu là một site có chứa abstract của bài báo đó.       Người cung cấp (đã log in vào tài khoản của trường) sau đó chỉ cần một động       tác click chuột là có thể tải xuống bài báo bạn cần. Bạn có thể lấy đường       link trực tiếp theo một trong các cách sau:</li>
